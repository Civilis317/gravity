package net.playground.gravity;

public class Point {
    double x;
    double y;

    // constructor
    Point() {
    }

    // constructor
    Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    // getters and setters
    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

}
